def area(a, h):
    '''
        Принимает числа a и h.
        Возвращает площадь треугольника (произведение a и h, делёное пополам ).
    '''
    return a * h / 2

def perimeter(a, b, c):
    '''
        Принимает числа a, b и c.
        Возвращает периметр треугольника (сумма a, b и c).
    '''
    return a + b + c
