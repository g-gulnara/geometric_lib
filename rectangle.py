def area(a, b):
    '''
    Принимает числа a и b.
    Возвращает площадь прямоугольника (произведение a и b).
    '''
    return a * b 

def perimeter(a, b):
    '''
    Принимает числа a и b.
    Возвращает периметр прямоугольника (удвоенная сумма его сторон a и b).
    '''
    return 2 * (a + b)
